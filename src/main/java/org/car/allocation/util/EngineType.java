package org.car.allocation.util;
/**
 * Represents the different types of engine available for vehicles.
 * <p>
 * This enum defines four types of engines:
 * </p>
 * <ul>
 * <li>{@link #PETROL} - Traditional internal combustion engine using petrol fuel.</li>
 * <li>{@link #DIESEL} - Internal combustion engine that runs on diesel fuel.</li>
 * <li>{@link #ELECTRIC} - Engine powered by electricity, typically used in electric vehicles (EVs).</li>
 * <li>{@link #HYBRID} - Engine that combines both an electric motor and an internal combustion engine.</li>
 * </ul>
 */
public enum EngineType {
    PETROL, DIESEL, ELECTRIC, HYBRID
}
